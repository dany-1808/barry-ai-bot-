
import re

def run(text):
    low = text.lower()

    if not ("анализ тренировок" in low or "тренировка" in low):
        return None

    # Используем регулярные выражения для извлечения веса, роста и количества тренировок
    pattern = r'(?:(масса|похудение|вес)\s*|)(\d+)\s*кг\s*(\d+)\s*см\s*(\d+)\s*раза?\s*в\s*неделю'
    match = re.search(pattern, low)
    
    if not match:
        return """
🏋️ Анализатор тренировок Barry

Укажи:
- цель (масса / похудение)
- вес
- рост
- возраст (опционально)
- сколько тренировок в неделю

Пример:
анализ тренировок масса 80 кг 180 см 4 раза в неделю
"""
    
    goal = match.group(1)
    weight = int(match.group(2))
    height = int(match.group(3))
    workouts_per_week = int(match.group(4))

    if goal in ["масса", "вес"]:
        return f"""
🏋️ Анализ набора массы:

✅ Белок: {1.6 * weight:.1f}-{2.2 * weight:.1f} г на кг веса
✅ Прогрессируй в рабочих весах
✅ Спи 7-9 часов
✅ Следи за питанием

Укажи возраст для более точного анализа.
"""
    
    if goal in ["похудение", "похуд", "жир"]:
        return f"""
🔥 Анализ похудения:

✅ Дефицит калорий 10-20%
✅ Белок держать высоким: {1.6 * weight:.1f}-{2.2 * weight:.1f} г на кг веса
✅ Силовые тренировки {3 if workouts_per_week > 3 else workouts_per_week}-5 раз в неделю
✅ Кардио по необходимости

Напиши возраст для более точного анализа.
"""

